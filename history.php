            	<h2>Our History</h2>
                <p>
                 More than half century has elapsed since the seed was sown in the little town of Chittaranjan and it has grown tremendously from strength and now stands as a mighty tree in the form of St. Joseph's Convent Higher Secondary School.</p>

<p>The Railway (Chittaranjan Locomotive Works) Administration approached Rev. Fr. Roeland SJ, the then Dean of Asansol, with a request for a group of Sisters to open a school at Chittaranjan. In response, His Grace Dr. Preier, the Archbishop op Kolkata, invited the Sisters of St. Joseph's of Chambery, Nagpur. Heeding the call, the two pioneers, Sisters Michael and Celine, arrived at Chittaranjan on a day of Holi, the festival of colours. Thus 13th March, 1952 became a red letter day for Chittaranjan. On 17th March 1962, They set to work with 27 students and two lady teachers laying the foundation of St. Joseph's Convent High School.</p>

<p>In 1964 the school had strength of 130 students and it obtained recognition for Primary Section from the Government of West Bengal. In 1965 the school was upgraded to Class VIII. The Government of West Bengal accorded recognition for Junior High School in January 1966. In December 1969, however, the good news of opening Class IX with 6 students was short lived as the Inspector of the Schools asked to close down Class IX due to inadequate accommodation.</p>

<p>However, on March 18, 1972, the Government of West Bengal recognized the school as the High School. This laurel infused in the missionaries an undaunted spirit of dedication, and they did not leave a single stone unturned to fulfill the dream of the people of Chittaranjan. The first batch of 5 studentswho appeared for ICSE examination came out successfully securing cent percent result.</p>

<p>It was in 1977, sensing the need of the institution growing in number and merit, the Railway Administration leased out a plot of land along Ashoke Avenue for construction of a new school building. Indeed it was a gift to St. Joseph's to celebrate the silver yester years of its existence. Till 1980, Sisters Michael, Celine, Josephine, Claire, Plus, Philippa and Ann Charlotte headed the institution with all its ups and downs. On 31st December 1980, the Sisters of St. Joseph of Chambery bid good-bye to St Joseph's Chittaranjan, which they had cradled for 29 years.</p>

<p>At the request of the Archbishop of Kolkata, the Sisters of Apostolic Carmel ventured to carry on the noble task of educating the children of Chittaranjan. On 2nd January 1981, the pioneering band of Apostolic Carmel Sisters Florita, Merita, Irene Pereira, Polycrap, Marcelia and Therese Alvaris took over the administration of the school.</p>

<p>The Sisters of Apostolic Carmel bid farewell to St. Joseph's Convent High School, nay, to Chittaranjan on 2nd July 1990 on completing the contract with the Archdiocese. A note of gratitude springs out from every heart for the marvellous achievements and the contribution made by the Sisters.</p>

<p>Since 1990, the growing institution has been taken care of by the Archbishop of Kolkata as invited the Sisters of the Little Flower of Bethany to carry on the Educational Apostolate. The pioneering group of Sisters was Edelburgh BS, Late Justina BS, Titus BS and Shwetha BS. The people, staff and the students accorded them a warm welcome. Sr. Edelburgh took over as Principal and Sister Cleopha, Sapientia BS, Ordilia BS and Caron BS succeeded her. In the year 2007, 2009 and 2010 ISC Science, Commerce and Arts (Humanities) Streams were introduced respectively along with computer E-Learning Digiclass for all the classes. Much care was taken to keep up excellence in all spheres. All round development, value based education, development of personality, computer education, revival of Interact Clubs and leadership Training were given prime importance.</p>

<p>The birth of Asansol Diocese, bifurcating from the Archdiocese of Kolkata was a boon. The timely care, guidance and constant support of the new Diocese enables the institution to sail safety amidst the stormy weather. The vision for future urged the school authority to approach the CLW for more land to extend the educational service to more students. In 1998, they leased out a plot of land in Ashoke Avenue to accommodate and extend quality education to children and the construction of school building for Primary was accomplished in 2002. Still the Institution kept growing in number and merit. There was more pressure on school authority to admit more students by opening one more section. The need of the hour was met with further extension of the school building with the facility of auditorium.</p>

<p>All through the journey, the Chittaranjan Locomotive Works administration saw that every need was met and every facility was provided within their limits.</p>

<p>May it inflame the josephites with added zeal, new insights and creative projects for an effective and rich contribution to keep the Mother Earth young and happy. 
                </p>
