            	<h2>Students Leadership & Participation </h2>

		<p>A leader must be a visionary and able to inspire innovation and change to provide high level strategic leadership for effective and efficient execution of ideas & views in every field. We, the Josephite indulge themselves in various clubs and communities.</p>

		<h3>Spiritual and Moral Guidance :</h3>
        <ul class="point">
        <li class="li-point"> Individual counseling is provided to the students, parents as well as teachers according to the need.</li>
        <li class="li-point"> Recollection</li>
        <li class="li-point"> Pilgrimages</li>
        <li class="li-point"> Special prayers are offered to God for the needy and the experiences of miracles are plentiful.</li>
		</ul>

<h3>Clubs and Associations :</h3>

<h4>Eco Club</h4>
<p>Eco club is rendering yeoman's service to the school community to build up strong awareness among the students about the challenges faced by Mother Earth in general.</p>
<p>Every student is given a small poster containing slogans explaining the responsibility for cleanliness of the school. The trees of the campus are named by the students after the great heroes of the world reminding that they have been and will be breathing for every Josephite the pure air and freshness of life.</p>
Joseph's campus wins the title 'Polythene Free Zone and Zero-litter Campus'

<h4>Co-curricular Club</h4>
<p>This club conducts activities such as extempore, elocution, debates, quiz, essay, poetry writing, creating stories, to help the students to break through the nutshells and gain confidence.</p>

<h4>Peace Club</h4>
<p>On peace day, the club members aim to promote the feeling of peace and banners and presenting greeting cards to the staff members for binding all of them in the bond of unity and prosperity.</p>

<h4>Paryavaran Mitra</h4>
<p>The Paryavaran Mitra programme is the initiative of CLE in partnership with the Ministry of Environment and Forest (MOEF) and Arcelor Mittal. The goal of the Paryavaran Mitra programme is to create a network of Young people across the Nation who have knowledge and awareness and commitment to meet the Challenge of global citizenship and climate change. In Paryavaran Mitra a child realizes the following :
<ul class="point">
<li class="li-point"> Plantation of trees.</li>
<li class="li-point"> To keep the house, surroundings and school campus clean.</li>
<li class="li-point"> Conservation of water.</li>
<li class="li-point"> Recycling of waste materials.</li>
<li class="li-point"> More use of renewable energy resources.</li>
</ul>
</p>

<h4>Media Club</h4>
<p>The members of Media club provide the Josephite with the news updates of economic, social, political spheres and sports. The members contribute to the news papers the writings on various current issues and are the makers of the News letter named "Josephite Panorama".</p>

<h4>Music Club</h4>
<p>With the academic year propelling, the school also gears up for one special feature of humans - that is to develop the latest talents of students in songs, dance and instrumental music. The International Music Day is commemorated in a befitting manner.</p>

<h4>Human Rights' Clubs</h4>
<p>The students have the right to protect their identity, needful assistance and keep themselves safe and sound. So the club members always stretch their helping hands to the needy and troubled to overcome the crisis period. 'Bethany Ekta Siksha Sansthan', the non-formal educational venture is supported by the school community wherein the unfortunate ones are provided with all the facilities to education and enjoy student-life like that of the day scholars. They are about 160 this year and are benefiting computer literacy from the generosity of the management.</p>

<h4>NSS</h4>
<p>The moto and aim of NSS is to help the poor and needy. So the club members collect old clothes and stationary and distribute among the poor students of the interior villages of West Bengal.</p>

<h4>Little Way Association</h4>
<p>The catholic students have a special role to play in devoting their time and energy in pleasing God in little ways with much love after the example of their patroness St. Therese - The Little flower of Jesus.</p>

<h4>Sports and Games Club</h4>
<p>The year has been a eventful year for the Josephites. The school has various games teams and added new feathers to its cap as number of matches viz. Football, Basket ball, Badminton, Table Tennis and so on were organized and the students proved their efficiency in every field. SJCHSS has achieved the supreme honour 'BEST ATHLETIC SCHOOL' among the 46 participating schools in the Regional Sports Meet 2011 of ASISC, West Bengal Chapter.
Excluding these clubs, there are several committees for the all round development of the students so that the banner of St. Joseph's fly higher and higher in the days to come.</p>


